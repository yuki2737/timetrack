export const CATALOG: Record<AppLocale, Record<string, string>> = { en, ja }

export function translate(
  locale: AppLocale,
  key: string,
  vars?: Record<string, string | number>,
): string {
  let s = CATALOG[locale]?.[key] ?? CATALOG.en[key] ?? key
  if (vars) {
    for (const [k, v] of Object.entries(vars)) {
      s = s.replaceAll(`{${k}}`, String(v))
    }
  }
  return s
}
